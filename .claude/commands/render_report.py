def render_doi_table(suggestions):
    if not suggestions:
        return ''
    confidence_icons = {
        'high': '<span class="status-pass">&#10003;</span>',
        'moderate': '<span class="status-info">&#8505;</span>',
        'low': '<span class="status-fail">&times;</span>',
    }
    rows = []
    for s in suggestions:
        ref = esc(s.get('reference', ''))
        doi = s.get('doi', '')
        doi_url = s.get('doi_url', f'https://doi.org/{doi}')
        conf = s.get('confidence', 'moderate').lower()
        icon = confidence_icons.get(conf, confidence_icons['moderate'])
        rows.append(
            f'    <tr>\n'
            f'      <td>{ref}</td>\n'
            f'      <td style="text-align: left;"><a href="{esc(doi_url)}">{esc(doi)}</a></td>\n'
            f'      <td>{icon}</td>\n'
            f'    </tr>'
        )
    return (
        '<table class="review-table" style="font-size: 13px;">\n'
        '  <thead>\n'
        '    <tr><th>REFERENCE</th><th style="text-align: left;">SUGGESTED DOI</th><th>CONFIDENCE</th></tr>\n'
        '  </thead>\n'
        '  <tbody>\n'
        + '\n'.join(rows) + '\n'
        '  </tbody>\n'
        '</table>\n'
        '<p style="font-size: 12px; color: #718096;">Confidence based on CrossRef match scores from metacheck.</p>'
    )


def render_statcheck_table(results):
    if not results:
        return ''
    rows = []
    for r in results:
        st = r.get('status', 'pass').lower()
        icon = '<span class="status-pass">&#10003;</span>' if st == 'pass' else '<span class="status-fail">&times;</span>'
        rows.append(
            f'    <tr>\n'
            f'      <td style="font-size: 13px;">{esc(r.get("reported", ""))}</td>\n'
            f'      <td style="font-size: 13px;">{esc(r.get("computed", ""))}</td>\n'
            f'      <td>{icon}</td>\n'
            f'    </tr>'
        )
    return (
        '<table class="review-table" style="margin-top: 12px;">\n'
        '  <thead>\n'
        '    <tr><th>TEST</th><th>REPORTED</th><th>STATUS</th></tr>\n'
        '  </thead>\n'
        '  <tbody>\n'
        + '\n'.join(rows) + '\n'
        '  </tbody>\n'
        '</table>'
    )


def render_replication_results(results):
    if not results:
        return ''
    parts = []
    for r in results:
        orig = esc(r.get('original', ''))
        repl = esc(r.get('replication', ''))
        context = r.get('context_note', '')
        parts.append('<div class="issue-card no-break">')
        parts.append(f'  <p><strong>Original:</strong> {orig}</p>')
        parts.append(f'  <p><strong>Replication:</strong> {repl}</p>')
        if context:
            parts.append(f'  <p style="font-size: 13px; color: #718096; margin-top: 8px;">{md_bold(context)}</p>')
        parts.append('</div>')
    return '\n'.join(parts)


def clean_metacheck_report(report):
    """Extract clean summary text from a metacheck report field (may be list or string)."""
    if isinstance(report, list):
        parts = []
        for item in report:
            text = str(item).strip()
            # Skip entire items that are code blocks
            if '```{r}' in text or text.startswith('```') or 'structure(list(' in text:
                continue
            # Take first meaningful line
            for line in text.split('\n'):
                line = line.strip()
                if line and not line.startswith('```') and not line.startswith('#|') and not line.startswith('# ') and not line.startswith('>'):
                    parts.append(line.rstrip(':').rstrip())
                    break
        return '; '.join(parts) if parts else ''
    return str(report).strip()


