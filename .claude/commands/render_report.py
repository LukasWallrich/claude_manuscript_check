def inject_metacheck(data, metacheck):
    """Inject metacheck data into check_categories and return pre-generated snippets."""
    # Only structured tables are pre-rendered as HTML snippets.
    # Narrative content (retraction, open science, p-values, marginal notes)
    # stays in metacheck_info for the LLM to write proper narratives.
    snippets = {
        'doi_table': '',
        'statcheck_table': '',
        'replication_results': '',
    }

    if not metacheck or 'results' not in metacheck:
        return snippets

    results = metacheck['results']
    cats = {c['number']: c for c in data.get('check_categories') or []}

    # --- Category 7: Referencing ---
    cat7 = cats.get(7)
    if cat7:
        mc = cat7.setdefault('metacheck_info', {}) or {}
        cat7['metacheck_info'] = mc

        # DOI suggestions with filtering
        doi_exclusions = set(mc.get('doi_exclusions') or [])
        doi_check = results.get('ref_doi_check', {}).get('table') or []
        doi_suggestions = []
        for entry in doi_check:
            doi = entry.get('DOI')
            ref_raw = entry.get('ref', '')

            # Filter GROBID artifacts
            if '(????)' in ref_raw:
                continue
            # Filter entries without a parenthesised year
            if not re.search(r'\(\d{4}\)', ref_raw):
                continue
            if not doi or doi in doi_exclusions:
                continue

            score = entry.get('score', 0)
            if score >= 60:
                conf = 'high' if score > 100 else 'moderate'
            else:
                conf = 'low'

            # Clean reference text: strip all HTML tags and decode entities
            ref_text = re.sub(r'<[^>]+>', '', ref_raw)
            ref_text = html_mod.unescape(ref_text)

            doi_suggestions.append({
                'reference': ref_text,
                'doi': doi,
                'doi_url': f'https://doi.org/{doi}',
                'confidence': conf,
            })

        if doi_suggestions:
            mc['doi_suggestions'] = doi_suggestions
            snippets['doi_table'] = render_doi_table(doi_suggestions)

        # Retraction summary — clean text for the LLM to narrate
        retraction = results.get('ref_retraction', {})
        pubpeer = results.get('ref_pubpeer', {})
        ret_text = clean_metacheck_report(retraction.get('report', ''))
        pubpeer_text = clean_metacheck_report(pubpeer.get('report', ''))
        # Deduplicate shared prefix like "We checked N references with DOIs."
        if ret_text and pubpeer_text:
            # Find first sentence of each
            ret_first = ret_text.split('. ')[0] + '.'
            pub_first = pubpeer_text.split('. ')[0] + '.'
            if ret_first == pub_first:
                # Strip shared prefix from pubpeer
                pubpeer_text = pubpeer_text[len(pub_first):].strip()
            summary = f"{ret_text} {pubpeer_text}".strip()
        else:
            summary = ret_text or pubpeer_text
        if summary and not mc.get('retraction_summary'):
            mc['retraction_summary'] = summary

        # Replication results
        repl = results.get('ref_replication', {}).get('table') or []
        if repl:
            repl_results = []
            for r in repl:
                repl_results.append({
                    'original': r.get('original_ref', r.get('ref', '')),
                    'replication': r.get('replication_ref', r.get('flora_ref', '')),
                    'context_note': r.get('context', None),
                })
            if not mc.get('replication_results'):
                mc['replication_results'] = repl_results
            snippets['replication_results'] = render_replication_results(repl_results)

    # --- Category 12: Statistical Reporting ---
    cat12 = cats.get(12)
    if cat12:
        mc = cat12.setdefault('metacheck_info', {}) or {}
        cat12['metacheck_info'] = mc

        # Statcheck results
        stat_check = results.get('stat_check', {}).get('table') or []
        if stat_check:
            sc_results = [
                {
                    'reported': s.get('raw', ''),
                    'computed': f"Computed p = {s.get('computed_p', 'N/A')}",
                    'status': 'fail' if s.get('error') else 'pass',
                }
                for s in stat_check
            ]
            if not mc.get('statcheck_results'):
                mc['statcheck_results'] = sc_results
            snippets['statcheck_table'] = render_statcheck_table(sc_results)

        # P-value summary — stored in metacheck_info for LLM to narrate
        p_vals = results.get('all_p_values', {}).get('table') or []
        if p_vals and not mc.get('p_value_summary'):
            mc['p_value_summary'] = f"{len(p_vals)} p-values detected in the manuscript."

        # Marginal significance — stored in metacheck_info for LLM to narrate
        marginal = results.get('marginal', {}).get('table') or []
        if marginal and not mc.get('marginal_notes'):
            mc['marginal_notes'] = f"{len(marginal)} instance(s) of marginal significance language found."

    # --- Category 10: Funding & Compliance ---
    cat10 = cats.get(10)
    if cat10:
        mc = cat10.setdefault('metacheck_info', {}) or {}
        cat10['metacheck_info'] = mc

        open_sci = []
        op = results.get('open_practices', {})
        if op.get('report'):
            open_sci.append(f"Open practices: {clean_metacheck_report(op['report'])}")
        prereg = results.get('prereg_check', {})
        if prereg.get('report'):
            open_sci.append(f"Preregistration: {clean_metacheck_report(prereg['report'])}")
        coi = results.get('coi_check', {})
        if coi.get('report'):
            open_sci.append(f"COI statement: {clean_metacheck_report(coi['report'])}")
        funding = results.get('funding_check', {})
        if funding.get('report'):
            open_sci.append(f"Funding statement: {clean_metacheck_report(funding['report'])}")
        if open_sci and not mc.get('open_science'):
            mc['open_science'] = open_sci

    return snippets


# ---------------------------------------------------------------------------
# prepare: Generate render context for LLM
# ---------------------------------------------------------------------------

def prepare(review_path, metacheck_path, output_path):
    with open(review_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    metacheck = None
    if metacheck_path:
        try:
            with open(metacheck_path, 'r', encoding='utf-8') as f:
                metacheck = json.load(f)
        except (FileNotFoundError, json.JSONDecodeError) as e:
            print(f"Warning: Could not load metacheck data: {e}", file=sys.stderr)

    # Inject metacheck and get pre-generated snippets
    snippets = inject_metacheck(data, metacheck)

    # Build HTML prefix (everything up through section review table)
    css = load_css()
    prefix_parts = [
        '<!DOCTYPE html>',
        '<html lang="en">',
        '<head>',
        '<meta charset="UTF-8">',
        '<meta name="viewport" content="width=device-width, initial-scale=1.0">',
        '<title>Manuscript Review Report</title>',
        f'<style>\n{css}</style>',
        '</head>',
        '<body>',
        '',
        render_header(data),
        '',
        render_about(data),
        '',
        render_summary(data),
        '',
        render_language_quality(data),
        '',
        render_section_review_table(data),
        '',
    ]

    # Generate count badges for each category
    category_badges = {}
    for cat in data.get('check_categories') or []:
        n = cat.get('number', 0)
        all_issues = (cat.get('issues') or [])
        for sub in (cat.get('subsections') or []):
            all_issues = all_issues + (sub.get('issues') or [])
        category_badges[str(n)] = count_badges(all_issues)

    context = {
        'html_prefix': '\n'.join(prefix_parts),
        'html_suffix': '\n</body>\n</html>',
        'snippets': snippets,
        'category_badges': category_badges,
        'check_categories': data.get('check_categories') or [],
    }

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(context, f, ensure_ascii=False, indent=2)

    print(f"Render context written to {output_path}", file=sys.stderr)


# ---------------------------------------------------------------------------
# assemble: Combine prefix + LLM body + suffix
# ---------------------------------------------------------------------------

def postprocess_body(body):
    """Fix common LLM output issues in the body HTML."""
    # Convert **bold** markers to <strong> tags (LLMs sometimes leave these unconverted)
    body = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', body)
    return body


def assemble(context_path, body_path, output_path):
    with open(context_path, 'r', encoding='utf-8') as f:
        context = json.load(f)

    with open(body_path, 'r', encoding='utf-8') as f:
        body = f.read()

    body = postprocess_body(body)
    html_content = context['html_prefix'] + '\n' + body + '\n' + context['html_suffix']

    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(html_content)

    print(f"Report written to {output_path}", file=sys.stderr)


