def render_about(data):
    mc = data.get('metadata', {}).get('metacheck_available', False)
    text = ABOUT_WITH_METACHECK if mc else ABOUT_WITHOUT_METACHECK
    return (
        f'<div class="about-box">\n'
        f'  <h2>ABOUT THIS REPORT</h2>\n'
        f'  <p>{text}</p>\n'
        f'</div>'
    )


def render_summary(data):
    summary = data.get('overall_summary', {})
    text = esc(summary.get('text', ''))
    c = summary.get('critical_count', 0)
    m = summary.get('major_count', 0)
    mi = summary.get('minor_count', 0)
    parts = [
        '<h2 class="section-title">OVERALL SUMMARY</h2>',
        '<hr class="section-divider">',
        f'<p class="summary-text">{text}</p>',
        '<div class="stats-row">',
        '  <div class="stat-box">',
        '    <div class="stat-label">CRITICAL ISSUES</div>',
        f'    <div class="stat-number critical">{c}</div>',
        '  </div>',
        '  <div class="stat-box">',
        '    <div class="stat-label">MAJOR ISSUES</div>',
        f'    <div class="stat-number major">{m}</div>',
        '  </div>',
        '  <div class="stat-box">',
        '    <div class="stat-label">MINOR ISSUES</div>',
        f'    <div class="stat-number minor">{mi}</div>',
        '  </div>',
        '</div>',
    ]

    # Critical issues inline
    for issue in data.get('top_critical_issues') or []:
        parts.append(
            f'<div class="issue-card">\n'
            f'  <strong>{esc(issue.get("title", ""))}</strong> &mdash; {esc(issue.get("category", ""))}<br>\n'
            f'  <span style="font-size: 14px; color: #4a5568;">{esc(issue.get("details", ""))}</span>\n'
            f'</div>'
        )

    return '\n'.join(parts)


def render_language_quality(data):
    lq = data.get('language_quality', {})
    overall = lq.get('overall_grade', 'N/A')
    badge_css = grade_badge_css(overall)

    parts = [
        '<h2 class="section-title">LANGUAGE QUALITY</h2>',
        '<hr class="section-divider">',
        f'<p class="summary-text">\n'
        f'  Overall language quality: <span class="overall-grade {badge_css}">{grade_display(overall)}</span>\n'
        f'</p>',
    ]

    dims = lq.get('dimensions') or []
    for dim in dims:
        grade = dim.get('grade', 'N/A')
        card_extra = grade_card_css(grade)
        parts.append(
            f'<div class="grade-card{card_extra}">\n'
            f'  <div>\n'
            f'    <div class="grade-title">{esc(dim.get("name", ""))}</div>\n'
            f'    <div class="grade-desc">{esc(dim.get("description", ""))}</div>\n'
            f'  </div>\n'
            f'  <div class="grade-badge {grade_badge_css(grade)}">{grade_display(grade)}</div>\n'
            f'</div>'
        )

    strengths = lq.get('key_strengths') or []
    if strengths:
        parts.append('<strong class="label">Key Strengths</strong>')
        parts.append('<ul class="strengths-list">')
        for s in strengths:
            parts.append(f'  <li>{esc(s)}</li>')
        parts.append('</ul>')

    improvements = lq.get('areas_for_improvement') or []
    if improvements:
        parts.append('<strong class="label">Areas for Improvement</strong>')
        parts.append('<ul class="improvements-list">')
        for i in improvements:
            parts.append(f'  <li>{esc(i)}</li>')
        parts.append('</ul>')

    return '\n'.join(parts)


def render_section_review_table(data):
    categories = data.get('check_categories') or []
    sections = data.get('section_review') or []

    # If no explicit section_review, derive from check_categories
    if not sections:
        section_descs = {
            1: "Clarity, length, accuracy",
            2: "Hierarchy, consistency, formatting",
            3: "Author details, affiliations, compliance statements",
            4: "Structure, completeness, length",
            5: "Overall organization, section order",
            6: "Captions, cross-references, formatting",
            7: "APA format, DOIs, retraction checks",
            8: "Grammar, spelling, style",
            9: "Definition on first use, consistency",
            10: "Ethics, COI, data availability, open science",
            11: "Causal language, limitations, sample description",
            12: "APA format, statcheck, p-values",
        }
        for cat in categories:
            n = cat.get('number', 0)
            issues = cat.get('issues') or []
            obs = cat.get('content_observations') or []
            sub_issues = []
            for sub in (cat.get('subsections') or []):
                sub_issues.extend(sub.get('issues') or [])
            total = len(issues) + len(sub_issues)

            # Determine status
            if total > 0:
                st = 'fail'
            elif cat.get('metacheck_info') or cat.get('subsections'):
                st = 'info'
            else:
                st = 'pass'

            sections.append({
                'name': f"{n}. {cat.get('name', '')}",
                'description': section_descs.get(n, ''),
                'status': st,
                'issue_count': total,
                'display_count': str(total) if total > 0 else ('Observations' if obs and total == 0 and n == 11 else str(total)),
            })

    rows = []
    for sec in sections:
        rows.append(
            f'    <tr>\n'
            f'      <td>\n'
            f'        <div class="table-section-name">{esc(sec.get("name", ""))}</div>\n'
            f'        <div class="table-section-desc">{esc(sec.get("description", ""))}</div>\n'
            f'      </td>\n'
            f'      <td>{status_icon(sec.get("status", "pass"))}</td>\n'
            f'      <td>{issue_count_display(sec)}</td>\n'
            f'    </tr>'
        )
    return (
        '<h2 class="section-title">SECTION REVIEW</h2>\n'
        '<hr class="section-divider">\n\n'
        '<table class="review-table">\n'
        '  <thead>\n'
        '    <tr><th>SECTION</th><th>STATUS</th><th>ISSUES</th></tr>\n'
        '  </thead>\n'
        '  <tbody>\n'
        + '\n'.join(rows) + '\n'
        '  </tbody>\n'
        '</table>'
    )


